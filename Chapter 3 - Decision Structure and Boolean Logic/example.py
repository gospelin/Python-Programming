# Multiple Nested Decision Structures
# Dr. Suarez teaches a literature class and uses the following 10-point grading scale for all of
# his exams:
#   Test Score Grade
#     90 and above    A
#     80–89           B
#     70–79           C
#     60–69           D
#     Below 60        F

#Get the Grade of the student
grade = int(input("Enter the student grade: "))

#Decision structure to determine the Grade
if grade >= 90:
    print("A")
elif grade >= 80:
    print("B")
elif grade >= 70:
    print("C")
elif grade >= 60:
    print("D")
else:
    print("F")
    

    
    
    